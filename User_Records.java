    private void loadtable() throws IOException{
        modify fh = new modify();
        User read = new User();
        String path = "login.txt";
        int line = fh.readLineNumber(path), n=0;
        DefaultTableModel model = (DefaultTableModel)(user.getModel());
        model.setRowCount(0);
        try {
            String[] id = read.readID(path);
            String[] name = read.readName(path);
            String[] usernamee = read.readUsername(path);
            String[] status = read.readStatus(path);
            //model.addRow(new Object[]{usernamee[0], usernamee[0], level[0],level[0]});
            while(n < line){
                model.addRow(new Object[]{id[n], name[n], usernamee[n],status[n]});
                n=n+1;
            }
            user.setModel(model);
        } catch (Exception e){
            
        }
    }
    
    private void search() throws IOException{
        User u = new User();
        String path = "login.txt";
        int id = u.searchUser(search.getText());
        if(id ==  -1){
            JOptionPane.showMessageDialog(null, "Username not found!");
        } else {
        DefaultTableModel model = (DefaultTableModel)(user.getModel());
        model.setRowCount(0);
        String[] idi = u.readID(path);
        String[] name = u.readName(path);
        String[] usernamee = u.readUsername(path);
        String[] status = u.readStatus(path);
        
        model.addRow(new Object[]{idi[id], name[id], usernamee[id], status[id]});
        user.setModel(model);
    }
    }
    
