    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            Centre c = new Centre();
            VaccineSupply vc = new VaccineSupply();
            boolean duplicate = c.checkDuplicate(name.getText());
            boolean availability = c.checkAvailability(vname.getSelectedItem().toString(), Integer.parseInt(qty.getText()));
            if(duplicate == true){
                JOptionPane.showMessageDialog(null, "Venue has already been registered!");
            } else {
                if(availability == true){
                 JOptionPane.showMessageDialog(null, "Quantity of vaccine must be at least 100 or exceed the number of distribution to proceed!");   
                } else{
                String path = "centre.txt";
                String [] input = {id.getText(),name.getText(),(String)vname.getSelectedItem(), qty.getText()};
                if(input[0].equals("")||input[1].equals("")||input[2].equals("")||input[3].equals("")){
                    JOptionPane.showMessageDialog(null, "Incomplete credentials, you have not fill one or more information.");
                } else {
                    try {
                        c.appendCentre(path, input);
                        vc.updateVaccine(input, Integer.parseInt(qty.getText()));
                        JOptionPane.showMessageDialog(null, "Saved successfully!\nPress REFRESH to update table.");
                        dispose();
                    } catch (IOException ex) {
                        
                    }
                }
            }
        } } catch (IOException ex) {
            Logger.getLogger(Add_Venue.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

