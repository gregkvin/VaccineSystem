    private void loadtable() throws IOException{
        modify fh = new modify();
        Appointment read = new Appointment();
        String path = "appointment.txt";
        int line = fh.readLineNumber(path), n=0;
        DefaultTableModel model = (DefaultTableModel)(appointmnt.getModel());
        model.setRowCount(0);
        try {
            String[] ID = read.readID();
            String[] Date = read.readDate();
            String[] Time = read.readTime();
            String[] Ctr = read.readCtr();
            String[] Rem = read.readRemarks();
            while(n < line){
                model.addRow(new Object[]{ID[n], Date[n], Time[n], Ctr[n], Rem[n]});
                n=n+1;
            }
            appointmnt.setModel(model);
            
        } catch (Exception e){
            
        }
    }
        
    private void search() throws IOException{
            Appointment a = new Appointment();
            int id = a.searchID(searchfield.getText());
            if(id ==  -1){
                JOptionPane.showMessageDialog(null, "Username not found!");
            } else {
                DefaultTableModel model = (DefaultTableModel)(appointmnt.getModel());
                model.setRowCount(0);
                String[] ID = a.readID();
                String[] Date = a.readDate();
                String[] Time = a.readTime();
                String[] Ctr = a.readCtr();
                String[] Rem = a.readRemarks();

        
                model.addRow(new Object[]{ID[id], Date[id], Time[id], Ctr[id], Rem[id]});
                appointmnt.setModel(model);
            }
          }

     private void searchStatus() throws IOException{
            Appointment a = new Appointment();
            ArrayList<Integer> rmk = a.searchRemarks(attribute1.getSelectedItem().toString());
            int n = 0;
            DefaultTableModel model = (DefaultTableModel)(appointmnt.getModel());
            model.setRowCount(0);
            String[] ID = a.readID();
            String[] Date = a.readDate();
            String[] Time = a.readTime();
            String[] Ctr = a.readCtr();
            String[] Rem = a.readRemarks();
    
            while(n < rmk.size()){
                model.addRow(new Object[]{ID[rmk.get(n)], Date[rmk.get(n)], Time[rmk.get(n)], Ctr[rmk.get(n)], Rem[rmk.get(n)]});
                n = n + 1;
                System.out.println(n);
            }
            appointmnt.setModel(model);
            
        }

    
  

