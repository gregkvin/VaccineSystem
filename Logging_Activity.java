    private void logout() throws IOException{
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");  
        LocalDateTime now = LocalDateTime.now(); 
        FileWrite fh = new FileWrite();
        String[] input = {jMenu1.getText(), "User logout successful.", dtf.format(now)};
        fh.LoggingActivity(input);
    }
    
    private void loadtable() throws IOException{
        FileWrite fh = new FileWrite();
        String path = "logging.txt";
        String [] username = fh.readUser(path);
        String [] remarks = fh.readRemarks(path);
        String [] time = fh.readTime(path);
        DefaultTableModel model = (DefaultTableModel)(logging.getModel());
        model.setRowCount(0); 
        int n = 0;
        int line = fh.readLineNumber(path);
        try {
            while(n < line){
                model.addRow(new Object[]{username[n], remarks[n], time[n]});
                n=n+1;
            }
            logging.setModel(model);
        } catch (Exception e){
            
        }
    }
        
    

    

